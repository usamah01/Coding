
public class ForLoopTest
{  
    public void displayNumbers()
    {
        
        for (int i=9;i>=0;i--)
        {
            System.out.println("the value of i is "  + i);
        }
    }
    
    
    public static void main(String args[])
    {
    	
        ForLoopTest f=new ForLoopTest();
    	f.displayNumbers();
    	
    }
}
