
public class Student
{
   
    private String id;
    public String name;

  
    public Student(String id,String name)
    {
        
        this.id = id;
        this.name=name;
    }

   
    public String getid()
    {
      
        return id;
    }
    
    public String getName()
    {
       
        return name;
    }
    
}
